# 案号正则表达式模式(模块级别常量)
# 案号格式:(年份)法院代码+案件类型+序号+号
# 例如:(2024)京0105民初12345号、(2024)粤0604民初41257号
CASE_NUMBER_PATTERNS = [
    # 标准格式:(2024)京0105民初12345号(中文括号)
    r"((\d{4}))([^\s()\(\)]{2,20}?)(\d+)号",
    # 标准格式:(2024)京0105民初12345号(英文括号)
    r"\((\d{4})\)([^\s()\(\)]{2,20}?)(\d+)号",
    # 无括号格式:2024京0105民初12345号(年份直接跟法院代码)
    r"(\d{4})([^\d\s]{1}[^\s()\(\)]{1,19}?)(\d+)号",
]
